public class Calculator {

    public int tambahNilai(int nilaiA, int nilaiB) {
        return nilaiA + nilaiB;
    }

    public int kurangNilai(int nilaiA, int nilaiB) {
        return nilaiA - nilaiB;
    }

    public float bagiNilai(int nilaiA, int nilaiB) {
        return nilaiA / nilaiB;
    }

    public int kaliNilai(int nilaiA, int nilaiB) {
        return nilaiA * nilaiB;
    }
}
